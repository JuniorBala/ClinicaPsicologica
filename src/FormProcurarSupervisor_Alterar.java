    private void btnProcurarSupervisorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcurarSupervisorActionPerformed
        String txt1 = txtNomeSupervisor.getText();
        String txt2 = txtDRT.getText();
        if (rbNome.isSelected() && txt1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o NOME DO SUPERVISOR !!!", "Erro", JOptionPane.WARNING_MESSAGE);
            txtNomeSupervisor.grabFocus();
        } else if (rbDRT.isSelected() && txt2.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o DRT !!!", "Erro", JOptionPane.WARNING_MESSAGE);
            txtDRT.grabFocus();
        } else {
            if(rbNome.isSelected()){
                retornoNome = txtNomeSupervisor.getText();
            }
            if(rbDRT.isSelected()){
                retornoDRT = txtDRT.getText();
            }
            groupButton.clearSelection();
            txtNomeSupervisor.setText("");
            txtDRT.setText("");
            FormAlterarSupervisor alterarSupervisor = new FormAlterarSupervisor();
            alterarSupervisor.setLocationRelativeTo(null);
            alterarSupervisor.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnProcurarSupervisorActionPerformed

