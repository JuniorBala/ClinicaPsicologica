    private void txtNomeSupervisorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomeSupervisorKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isLetter(c)){
            if(!Character.isWhitespace(c)){
                evt.consume();
            }
        }
        int x = 0;
        String nome = txtNomeSupervisor.getText();
        x = nome.length();
        if(x == 45){
            evt.consume();
        }
    }//GEN-LAST:event_txtNomeSupervisorKeyTyped

