    private void btnProcurarAtendenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcurarAtendenteActionPerformed
        String txt1 = txtNomeAtendente.getText();
        String txt2 = txtTIA.getText();
        if (rbNome.isSelected() && txt1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o NOME DO ATENDENTE !!!", "Erro", JOptionPane.WARNING_MESSAGE);
            txtNomeAtendente.grabFocus();
        } else if (rbTIA.isSelected() && txt2.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o TIA !!!", "Erro", JOptionPane.WARNING_MESSAGE);
            txtTIA.grabFocus();
        } else {
            if(rbNome.isSelected()){
                retornoNome = txtNomeAtendente.getText();
            }
            if(rbTIA.isSelected()){
                retornoTIA = txtTIA.getText();
            }
            groupButton.clearSelection();
            txtNomeAtendente.setText("");
            txtTIA.setText("");
            FormRealizarAvaliacao realizarAvaliacao = new FormRealizarAvaliacao();
            realizarAvaliacao.setLocationRelativeTo(null);
            realizarAvaliacao.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnProcurarAtendenteActionPerformed

