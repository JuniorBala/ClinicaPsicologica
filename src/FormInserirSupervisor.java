    private void btnInserirSupervisorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirSupervisorActionPerformed
        String txt1 = txtNomeSupervisor.getText();
        String txt2 = txtDRT.getText();
        String txt3 = txtEndereco.getText();
        String txt4 = txtCEP.getText();
        String txt5 = txtTelefone.getText();
        if (txt1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o NOME DO SUPERVISOR !!!", "Erro", JOptionPane.WARNING_MESSAGE);
            txtNomeSupervisor.grabFocus();
        } else if (txt2.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o DRT !!!", "Erro", JOptionPane.WARNING_MESSAGE);
            txtDRT.grabFocus();
        } else if (txt3.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o ENDEREÇO !!!", "Erro", JOptionPane.WARNING_MESSAGE);
            txtEndereco.grabFocus();
        } else if (txt4.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o CEP !!!", "Erro", JOptionPane.WARNING_MESSAGE);
            txtCEP.grabFocus();
        } else if (txt5.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o TELEFONE !!!", "Erro", JOptionPane.WARNING_MESSAGE);
            txtTelefone.grabFocus();
        } else {
            try {
            // fazer a conexão com o banco de dados
            Class.forName("com.mysql.jdbc.Driver");
            Connection c = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/Clinica", "root", "administrador");

            //definir um objeto para uso dos comandos SQL
            Statement st = c.createStatement();

            //obter os dados digitados
            String nome = txtNomeSupervisor.getText();
            String drt = txtDRT.getText();
            String endereco = txtEndereco.getText();
            String cep = txtCEP.getText();
            String telefone = txtTelefone.getText();
            
            ResultSet rs = st.executeQuery("SELECT * FROM Supervisor");
            // Ler cada registro da tabela para exibir no JTextArea
            while (rs.next()) {
                String drtSupervisor = rs.getString("DRT");
                if (drtSupervisor.equalsIgnoreCase(drt)) {
                    JOptionPane.showMessageDialog(null, "Este DRT já existe !!!", "Erro", JOptionPane.WARNING_MESSAGE);
                    txtDRT.setText("");
                    txtDRT.grabFocus();
                    return;
                }
                String nomeSupervisor = rs.getString("NomeSupervisor");
                if (nomeSupervisor.equalsIgnoreCase(nome)) {
                    JOptionPane.showMessageDialog(null, "Este NOME já existe !!!", "Erro", JOptionPane.WARNING_MESSAGE);
                    txtNomeSupervisor.setText("");
                    txtNomeSupervisor.grabFocus();
                    return;
                }
            }

            String comando = "INSERT INTO Supervisor (NomeSupervisor, DRT, Endereco, CEP, Telefone) VALUES('" + nome + "', '" + drt + "', '" + endereco + "', " + cep + ", '" + telefone + "')";

            st.execute(comando);
            JOptionPane.showMessageDialog(null, "Supervisor INCLUIDO com sucesso !");

            //fecha a conexão2
            st.close();
            c.close();

            limparCampos();
            txtNomeSupervisor.grabFocus();
            } catch (ClassNotFoundException erro) {
                JOptionPane.showMessageDialog(null, " Driver JDBC não encontrado !\n  " + erro.toString());
            } catch (SQLException erro) {
                JOptionPane.showMessageDialog(null, " Problemas na conexão com a fonte de dados!\n" + erro.toString());
            }
           }    
    }//GEN-LAST:event_btnInserirSupervisorActionPerformed

